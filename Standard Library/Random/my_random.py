import random  # not suitable to generate cryptoghraphy, uses secrets module instead

print(random.random())  # random method, between 0 (inclusive) and 1 (non-inclusive)
print(random.uniform(1, 10))  # floating value beween two numbers

print(random.randint(1, 6))  # random integer: both inclusive
print(random.randint(0, 1))  # simulating a coin

courses = ['History', 'Math', 'Physics', 'CompSci']
print(random.choice(courses))  # choice method: this method takes a list and returns a random member
colors = ['Red', 'Black', 'Green']
print(random.choices(colors, k=10))  # choices method: this method takes a list and the number of rounds and returns a list. all values in the list are equal in terms of the chance to be in the returning list. of course, if k is greater than the number of original list members, there will be duplicates for sure.
print(random.choices(colors, weights=[18, 18, 2], k=10))  # Red chance is 18/38, Black chance is 18/38 and Green is 2/38

deck = list(range(1, 52))
random.shuffle(deck)  # returns None
print(deck)
print(random.sample(deck, k=5))  # unlike choices method, this method only returns unique values. k cannot be greater 51.

